#!/usr/bin/env python3
"""Regenerate the frozen foundation benchmark fixtures.

The foundation benchmark is a grid of canonical Minkowski-diamond
sprinklings used to validate embedding algorithms. Because
:func:`validation_suite.sprinkle_minkowski_diamond` is deterministic
under a fixed seed, we do *not* freeze the causets themselves as
bytes; we freeze the protocol (the list of ``(d_spacetime, n, seed)``
tuples) together with the precomputed order-theoretic invariants for
each cell. The regression test verifies that recomputation from the
current code matches the frozen values, which catches any change to
the sprinkler or the invariant routines.

This script is the single source of truth for what cells the
foundation benchmark covers. Edit the ``CELLS`` constant below if
you need to extend it, then rerun this script to refresh
``benchmarks/foundation/invariants.json`` and the README.
"""

from __future__ import annotations

import json
import sys
from pathlib import Path

ROOT = Path(__file__).resolve().parents[1]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))

import causet_invariants  # noqa: E402  (sys.path adjusted above)
import validation_suite as vs  # noqa: E402
FOUNDATION = ROOT / "benchmarks" / "foundation"


# Foundation grid. Kept modest so the benchmark is cheap to recompute,
# but rich enough that ensemble statistics are well defined: 3
# spacetime dimensions, 3 sizes, 5 seeds per cell = 45 cells.
CELLS = [
    (d, n, seed)
    for d in (2, 3, 4)
    for n in (16, 32, 64)
    for seed in (1959, 1962, 1987, 2009, 2026)
]


def build_invariants() -> dict:
    """Compute the fingerprint for every cell in CELLS."""

    payload = {
        "version": 1,
        "description": (
            "Foundation benchmark for embedding validation. Each entry "
            "is a uniform Poisson sprinkling of the unit Minkowski "
            "causal diamond in d_spacetime dimensions, generated by "
            "validation_suite.sprinkle_minkowski_diamond(n, seed, "
            "d_spacetime). The invariants are computed by "
            "causet_invariants.invariants_fingerprint."
        ),
        "cells": [],
    }
    for d, n, seed in CELLS:
        matrix, _ = vs.sprinkle_minkowski_diamond(
            n=n, seed=seed, d_spacetime=d
        )
        fp = causet_invariants.invariants_fingerprint(matrix)
        payload["cells"].append({
            "d_spacetime": d,
            "n": n,
            "seed": seed,
            "fingerprint": fp,
        })
    return payload


def write_invariants_json(payload: dict) -> None:
    """Write the frozen invariants table to JSON."""

    FOUNDATION.mkdir(parents=True, exist_ok=True)
    out = FOUNDATION / "invariants.json"
    out.write_text(
        json.dumps(payload, indent=2, sort_keys=True) + "\n",
        encoding="utf-8",
    )


def write_readme(payload: dict) -> None:
    """Write a human-readable description of the foundation benchmark."""

    cells_by_dim: dict[int, list[dict]] = {}
    for cell in payload["cells"]:
        cells_by_dim.setdefault(cell["d_spacetime"], []).append(cell)

    lines = [
        "# Foundation Benchmarks",
        "",
        "This directory holds the frozen *protocol* of the foundation",
        "validation benchmark for causet embedding methods.",
        "",
        "The benchmark is a grid of uniform Poisson sprinklings of the",
        "unit Minkowski causal diamond. Each cell is identified by",
        "`(d_spacetime, n, seed)` and produced deterministically by",
        "`validation_suite.sprinkle_minkowski_diamond(n, seed, d_spacetime)`.",
        "Because the sprinkler is reproducible, we freeze only the",
        "precomputed order-theoretic invariants and let the actual",
        "causets be regenerated on demand.",
        "",
        "## Cells",
        "",
    ]
    for d in sorted(cells_by_dim):
        cells = cells_by_dim[d]
        sizes = sorted({c["n"] for c in cells})
        seeds = sorted({c["seed"] for c in cells})
        lines.append(f"### d_spacetime = {d}")
        lines.append("")
        lines.append(f"- sizes: {', '.join(str(s) for s in sizes)}")
        lines.append(f"- seeds: {', '.join(str(s) for s in seeds)}")
        lines.append(
            "- expected Myrheim-Meyer dimension: "
            f"{d} (closed form for unit diamond)"
        )
        lines.append("")

    # Sample fingerprint to illustrate the format.
    sample = payload["cells"][0]
    lines += [
        "## Frozen invariants",
        "",
        "`invariants.json` records the full fingerprint of each cell.",
        "A single entry looks like this:",
        "",
        "```json",
        json.dumps(sample, indent=2),
        "```",
        "",
        "The fields are described in `causet_invariants.py`.",
        "",
        "## Regenerating",
        "",
        "Run `python3 tools/build_foundation_benchmarks.py` from the",
        "repository root. The script overwrites `invariants.json` and",
        "this README. The accompanying regression test",
        "`tests/test_foundation_benchmarks.py` then verifies that the",
        "current code reproduces the frozen invariants bit for bit.",
        "",
        "## Why not freeze the matrices?",
        "",
        "Two reasons. First, the sprinkler is deterministic under a",
        "fixed seed, so the matrix is fully determined by the cell",
        "key; storing it again would be redundant and would invite",
        "drift. Second, freezing the *invariants* directly tests the",
        "scientifically meaningful claim: that the order-theoretic",
        "diagnostics of these reference causets are stable. A change",
        "to the sprinkler or the invariant computation is detected",
        "immediately, while incidental serialization changes do not",
        "perturb the benchmark.",
        "",
    ]
    (FOUNDATION / "README.md").write_text(
        "\n".join(lines), encoding="utf-8"
    )


def main() -> None:
    payload = build_invariants()
    write_invariants_json(payload)
    write_readme(payload)
    print(f"Wrote {len(payload['cells'])} cells to {FOUNDATION}")


if __name__ == "__main__":
    main()
